package org.pmoo.packproyecto.exceptions;

public class ImprisonedException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3039683545930349071L;

	public ImprisonedException() {
	}

	public ImprisonedException(String arg0) {
		super(arg0);
	}

	public ImprisonedException(Throwable arg0) {
		super(arg0);
	}

	public ImprisonedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ImprisonedException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
