package org.pmoo.packproyecto.exceptions;

public class UpgradeLimitException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4234528197305576271L;

	public UpgradeLimitException() {
	}

	public UpgradeLimitException(String arg0) {
		super(arg0);
	}

	public UpgradeLimitException(Throwable arg0) {
		super(arg0);
	}

	public UpgradeLimitException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UpgradeLimitException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
